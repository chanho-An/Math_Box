# 기본 계산기
def abb(a, b):
	return a + b

def substract(a, b):
	return a - b

def multiply(a, b):
	return a * b

def divide_new(a, b):
	return a / b

def square(a, b):
	return a ** b

def get_Percent(a, b):
	return (a / b) * 100

def remainder(a, b):
	return a // b

def get_Median(a, b):
	return (a + b) / 2
